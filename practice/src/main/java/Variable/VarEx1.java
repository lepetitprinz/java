package Variable;

public class VarEx1 {
    public static void main(String[] args) {
        int year = 2022;
        int age = 30;

        System.out.println(year);
        System.out.println(age);

        year = age + 2000;
        age = age + 1;

        System.out.println(year);
        System.out.println(age);
    }
}
